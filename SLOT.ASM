	LD	D,A	;基本スロットに出力する値
			;対象の拡張スロットを切り替えるために基本スロットを切り替える
	LD	A,H
	AND	3
	LD	C,A	;C=スロット#

	;スロットテーブル
	LD	L,LOW SLTTBL
	ADD	A,L
	LD	L,A
			;対象の拡張スロットを切り替えるために基本スロットを切り替える
	LD	A,C	;C=スロット#
	RRCA
	RRCA		;ページ3
	LD	C,A
	LD	A,D	;基本スロットに出力する値
	AND	03FH	;ページ3マスク
	OR	C
	LD	E,A	;基本スロットでページ3にスロットを割り当てる
	LD	A,H
	AND	00CH
	LD	H,HIGH SLTTBL
	RET
