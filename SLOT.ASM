AT_ISC:
	ORG	ISC,AT_ISC-RUN
;
; インタースロットコール
; ページ3 に配置

;
;	現在のスロットを知る
;in
;HL←上位2ビットで切り替えるページを指定
;out
;A→スロット
;※ROM、RAMの両方のルーチンを使うため絶対番地を使わない
_GETSLT:
	PUSH	HL
	IN	A,(0A8H)

	BIT	7,H
	JR	NZ,GETSLT_HIGH
	BIT	6,H
	LD	HL,EXPTBL
	JR	NZ,GETSLT_40H
GETSLT_00H:				;ページ0
	AND	3
	ADD	A,L
	LD	L,A
	BIT	7,(HL)
	JR	Z,GETSLT_1
	ADD	A,SLTTBL-EXPTBL		;拡張スロットをワークアリアから取得
	LD	L,A
	LD	A,(HL)
GETSLT_3:
	RLCA
	RLCA
GETSLT_2:
	AND	00CH
	LD	H,A
	LD	A,L
	SUB	LOW SLTTBL - 080H
	OR	H
	POP	HL
	RET
GETSLT_1:				;スロットは拡張されていない
	SUB	LOW EXPTBL
	POP	HL
	RET

GETSLT_40H:				;ページ1
	RRCA
	RRCA
	AND	3
	ADD	A,L
	LD	L,A
	BIT	7,(HL)
	JR	Z,GETSLT_1
	ADD	A,SLTTBL-EXPTBL
	LD	L,A
	LD	A,(HL)
	JR	GETSLT_2

GETSLT_HIGH:
	BIT	6,H
	LD	HL,EXPTBL
	JR	NZ,GETSLT_C0H
GETSLT_80H:				;ページ2
	RRCA
	RRCA
	RRCA
	RRCA
	AND	3
	ADD	A,L
	LD	L,A
	BIT	7,(HL)
	JR	Z,GETSLT_1
	ADD	A,SLTTBL-EXPTBL
	LD	L,A
	LD	A,(HL)
	RRCA
	RRCA
	JR	GETSLT_2

GETSLT_C0H:				;ページ3
	RLCA
	RLCA
	AND	3
	ADD	A,L
	LD	L,A
	BIT	7,(HL)
	JR	Z,GETSLT_1
	ADD	A,SLTTBL-EXPTBL
	LD	L,A
	LD	A,(HL)
	RLCA
	RLCA
	JR	GETSLT_3

